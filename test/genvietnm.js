const fs = require('fs');

let first_name_arr = firstname.split(',');
let boys_name_1_arr = boys_name_1.split(',');
let boys_name_2_arr = boys_name_2.split(',');

let girls_name_1_arr = girls_name_1.split(',');
let girls_name_2_arr = girls_name_2.split(',');

let vietnam_boys = [];
let vietnam_girls = [];
for(let i = 0; i< first_name_arr.length; ++i){
    for(let j=0; j< boys_name_1_arr.length; ++j){
        vietnam_boys.push(`${first_name_arr[i]} ${boys_name_1_arr[j]}`.replace('\\n',''));
    }

    for(let j=0; j< boys_name_2_arr.length; ++j){
        vietnam_boys.push(`${first_name_arr[i]} ${boys_name_2_arr[j]}`.replace('\\n',''));
    }

    for(let j=0; j< girls_name_1_arr.length; ++j){
        vietnam_girls.push(`${first_name_arr[i]} ${girls_name_1_arr[j]}`.replace('\\n',''));
    }

    for(let j=0; j< girls_name_2_arr.length; ++j){
        vietnam_girls.push(`${first_name_arr[i]} ${girls_name_2_arr[j]}`.replace('\\n',''));
    }
}

vietnam_boys.sort(function(a,b){
    return Math.random() > 0.5 ? -1 : 1;
})

vietnam_girls.sort(function(a,b){
    return Math.random() > 0.5 ? -1 : 1;
})

fs.writeFileSync('vietnam_boys.js', JSON.stringify(vietnam_boys));
fs.writeFileSync('vietnam_girls.js', JSON.stringify(vietnam_girls));

